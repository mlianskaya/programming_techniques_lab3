/**
 * @file rng_generators.cpp
 * @brief Реализация трёх модифицированных генераторов псевдослучайных чисел.
 * 
 * В этом файле реализованы три генератора:
 * - MLCG_XOR: модифицированный LCG с нелинейным перемешиванием.
 * - LFG_XOR: генератор Фибоначчи с запаздыванием и XOR.
 * - Xorshift128_MLT: модифицированный Xorshift128 с нелинейным умножением.
 */

#include "rng_generators.hpp"
#include <cstdint>

// 1. MLCG‑XOR: модифицированный LCG с нелинейным перемешиванием
// Принцип: классический LCG (X_{n+1} = a*X_n + c mod 2^64) даёт хорошую скорость,
//          но младшие биты имеют малый период и коррелированы.
// Модификация: после LCG-шага применяем нелинейное перемешивание (XOR + сдвиги),
//              которое «перетасовывает» биты, устраняя корреляцию и улучшая
//              статистические свойства.

/**
 * @brief Конструктор: сохраняет начальное состояние.
 * @param seed начальное значение (по умолчанию 123456789)
 */
MLCG_XOR::MLCG_XOR(uint64_t seed) : state(seed) {}

/**
 * @brief Генерирует следующее псевдослучайное 64-битное число.
 * @return 64-битное число, равномерно распределённое в [0, 2^64-1].
 * 
 * Шаги:
 * 1. Выполняется LCG-шаг: state = A * state + C.
 *    Переполнение uint64_t автоматически берёт результат по модулю 2^64.
 *    Параметры A и C подобраны так, чтобы период был максимальным (2^64).
 * 2. К полученному состоянию применяются три битовые операции:
 *    - x ^= (x >> 31)   // сдвиг вправо и XOR
 *    - x ^= (x << 25)   // сдвиг влево и XOR
 *    - x ^= (x >> 28)   // ещё один сдвиг вправо
 *    Эти операции нелинейны и хорошо перемешивают биты, разрушая
 *    корреляцию младших битов, свойственную LCG.
 */
uint64_t MLCG_XOR::next() {
    // LCG шаг: переполнение по модулю 2^64 происходит автоматически
    state = A * state + C;

    // Нелинейное перемешивание
    uint64_t x = state;
    x ^= (x >> 31);               // смешиваем старшие биты с младшими
    x ^= (x << 25);               // сдвиг влево 
    x ^= (x >> 28);               // финальное перемешивание
    return x;
}

// 2. LFG‑XOR: генератор Фибоначчи с запаздыванием и XOR
// Принцип: LFG использует рекуррентную формулу
//          X[n] = X[n-p] XOR X[n-q] (p=55, q=24). Такой выбор лагов даёт
//          большой период (~2^79) и хорошую случайность.
// Модификации:
//   - Вместо сложения используется XOR, что быстрее и даёт лучшее перемешивание битов.
//   - Добавлена функция scramble (XOR + сдвиги) для нелинейного преобразования
//     каждого нового значения, что улучшает статистические свойства.
//   - Инициализация буфера выполняется с помощью LCG, а затем делается несколько
//     проходов перемешивания, чтобы сделать генератор менее чувствительным к seed.

/**
 * @brief Нелинейное перемешивание 32-битного слова.
 * @param x входное 32-битное число.
 * @return перемешанное число.
 * 
 * Применяются три операции: сдвиг влево, сдвиг вправо и XOR.
 */
uint32_t LFG_XOR::scramble(uint32_t x) const {
    x ^= x << 13;
    x ^= x >> 17;
    x ^= x << 5;
    return x;
}

/**
 * @brief Инициализация буфера генератора.
 * @param seed начальное значение для инициализации.
 * 
 * Алгоритм:
 * 1. Заполняем буфер размера LAG1 (55) с помощью LCG (state = state*1103515245+12345).
 *    Каждое значение сразу пропускается через scramble для начального перемешивания.
 * 2. Затем 5 раз проходим по буферу: для каждого i берём элемент с отставанием
 *    j = (i + LAG2) % LAG1, делаем XOR и снова scramble.
 *    Это гарантирует, что все начальные значения хорошо перемешаны и нет
 *    зависимости от seed.
 */
void LFG_XOR::initBuffer(uint32_t seed) {
    uint32_t state = seed;
    // Первое заполнение: классический LCG
    for (int i = 0; i < LAG1; ++i) {
        state = state * 1103515245 + 12345;
        buffer[i] = scramble(state);
    }
    // Дополнительное перемешивание (5 проходов)
    for (int pass = 0; pass < 5; ++pass) {
        for (int i = 0; i < LAG1; ++i) {
            int j = (i + LAG2) % LAG1;   // индекс с запаздыванием
            buffer[i] ^= buffer[j];       // основная операция LFG (XOR)
            buffer[i] = scramble(buffer[i]); // дополнительное перемешивание
        }
    }
}

/**
 * @brief Конструктор LFG_XOR.
 * @param seed начальное значение (по умолчанию 123456789).
 */
LFG_XOR::LFG_XOR(uint32_t seed) : index(LAG1) {
    initBuffer(seed);
}

/**
 * @brief Генерирует следующее 32-битное псевдослучайное число.
 * @return случайное число.
 * 
 * Если индекс достиг конца буфера (index >= LAG1), то:
 *   - проходим по всему буферу, для каждого i обновляем buffer[i] по правилу
 *     buffer[i] ^= buffer[(i + LAG2) % LAG1], затем применяем scramble.
 *   - сбрасываем индекс на 0.
 * Затем возвращаем buffer[index++] и увеличиваем индекс.
 */
uint32_t LFG_XOR::next() {
    if (index >= LAG1) {
        // Генерация новой порции чисел
        for (int i = 0; i < LAG1; ++i) {
            int j = (i + LAG2) % LAG1;   // индекс с запаздыванием
            buffer[i] ^= buffer[j];       // рекуррентная формула LFG (XOR)
            buffer[i] = scramble(buffer[i]); // нелинейное перемешивание
        }
        index = 0;
    }
    return buffer[index++];
}

// 3. Xorshift128_MLT: модифицированный Xorshift с нелинейным умножением
// Принцип: Xorshift128 использует 128 бит состояния (два 64-битных слова)
//          и операции XOR и сдвигов. Период 2^128-1, очень высокая скорость.
//          Однако чисто линейный Xorshift может не проходить некоторые тесты.
// Модификация: после получения очередного состояния к результату применяется
//              нелинейное умножение на две константы
//              и дополнительные XOR-сдвиги. Это «ломает» линейность и
//              значительно улучшает статистическую случайность, сохраняя
//              скорость и период.

/**
 * @brief Конструктор Xorshift128_MLT.
 * @param seed начальное значение (по умолчанию 123456789).
 * 
 * Инициализирует два 64-битных слова состояния:
 *   s[0] = seed,
 *   s[1] = seed * константа (золотое сечение).
 * Затем делается 10 итераций next() для «прогрева» — чтобы начальные значения
 * хорошо перемешались и убрать возможную корреляцию.
 */
Xorshift128_MLT::Xorshift128_MLT(uint64_t seed) {
    s[0] = seed;
    s[1] = seed * 0x9e3779b97f4a7c15ULL; // константа «золотого сечения» для инициализации
    // Прогрев: несколько итераций, чтобы состояние стало менее предсказуемым
    for (int i = 0; i < 10; ++i) next();
}

/**
 * @brief Генерирует следующее 64-битное псевдослучайное число.
 * @return случайное число.
 * 
 * Шаги:
 * 1. Выполняется классический Xorshift128:
 *    - берём s1 = s[0], s0 = s[1]
 *    - обновляем s[0] = s0
 *    - выполняем серию XOR и сдвигов: s1 ^= s1<<23; s1 ^= s1>>17; s1 ^= s0; s1 ^= s0>>26
 *    - сохраняем s[1] = s1
 * 2. Применяем нелинейное преобразование к s1:
 *    - умножаем на константу 0xbf58476d1ce4e5b9ULL
 *    - делаем XOR со сдвигом вправо на 31 бит
 *    - умножаем на константу 0x94d049bb133111ebULL
 *    - делаем XOR со сдвигом вправо на 27 бит
 *    Эти операции взяты из генератора SplitMix64 и обеспечивают высокое качество
 *    случайности, сохраняя период исходного Xorshift.
 */
uint64_t Xorshift128_MLT::next() {
    // Классический Xorshift128
    uint64_t s1 = s[0];
    uint64_t s0 = s[1];
    s[0] = s0;
    s1 ^= s1 << 23;
    s1 ^= s1 >> 17;
    s1 ^= s0;
    s1 ^= s0 >> 26;
    s[1] = s1;

    // Нелинейное преобразование
    uint64_t result = s1 * 0xbf58476d1ce4e5b9ULL;
    result ^= (result >> 31);
    result *= 0x94d049bb133111ebULL;
    result ^= (result >> 27);
    return result;
}